/**
  *尺码提示信息悬浮框
  *@param obj 全局对象global_spec_arr[ good_id ]，good_id在调用函数中取得。
  *       global_spec_arr[goods_id].products_ids 尺码标签ID数组 类似 "goods_size_goods12082798131136_1408" 为组合后的的id，可以直接查找到元素
  *       global_spec_arr[goods_id].goods_tool_tips_type getGoodsTips函数参数，类似 女士睡衣 格式
  *
  *       ".goodsToolTips" 为悬浮div层
  *@author Yuliang Li
  */
function goodsSizeTips( obj )
{
  //type类似【女士睡衣上衣】
  var type = obj.goods_tool_tips_type;
  var goodsToolTips = getGoodsTips( type ) || getGoodsTips( type.substr( 0, 4 ) );
  if( !goodsToolTips )
    return false;
  $( "html" ).append( '<div class="toolTipsBody"><em class="toolTipsBodyIcon"></em><div class="toolTipsBodyCon"></div></div>' );
  $.each( obj.products_ids, function( i, id ){
    var index = $( "#" + id ).text();
    if( goodsToolTips && goodsToolTips[index] )
    {
      $( "#" + id ).bind( "mouseover mouseout", function( e ){
      if( e.type == "mouseover"  )
      {
        var str = "<h4>参考身材：</h4><p>";
        $.each( goodsToolTips[index], function( j, goodstooltip ){ str += j + "：" + goodstooltip + "<br />"; });
        str = str.substr( 0, str.length - 6 ) + "</p>";
        $( '.toolTipsBodyCon' ).html( str ).parent().css({ top: $( "#" + id ).offset().top + $( "#" + id ).height(), left: $( "#" + id ).offset().left - 60 + ( $( "#" + id ).width() / 2 ) }).show();
      }
      if( e.type == "mouseout" )
      {
        $( ".toolTipsBody" ).hide();
      }
      });
    }
  });
}
  
